using namespace std;

const int MAXN = 1e5;
const int INF = 1e18;
const int MOD = 1e9 + 7;

vector<int> gg(vector<int> a){
    int n = len(a) / 3;
    vector<int> ans(n + 1);
    multiset<int> k;
    int summ = 0;
    for (int i = 0; i < n; i++){
        summ += a[i];
        k.insert(a[i]);
    }
    ans[0] = summ;
    for (int i = 0; i < n; i++){
        k.insert(a[i + n]);
        summ += a[i + n];
        summ -= *k.begin();
        k.erase(k.begin());
        ans[i + 1] = summ;
    }
    return ans;
}

vector<int> gg1(vector<int> a){
    int n = len(a) / 3;
    vector<int> ans(n + 1);
    multiset<int> k;
    int summ = 0;
    for (int i = 0; i < n; i++){
        summ += a[i];
        k.insert(a[i]);
    }
    ans[0] = summ;
    for (int i = 0; i < n; i++){
        k.insert(a[i + n]);
        summ += a[i + n];
        auto d = k.end();
        d--;
        summ -= *d;
        k.erase(d);
        ans[i + 1] = summ;
    }
    return ans;
}

main() {
    ios_base::sync_with_stdio(0);
    int n;
    cin >> n;
    vector<int> a(3 * n);
    for (int i = 0; i < 3 * n; i++){
        cin >> a[i];
    }
    auto g = gg(a);
    reverse(all(a));
    auto g1 = gg1(a);
    reverse(all(g1));
    int ans = -INF;
    for (int i = 0; i <= n; i++){
        ans = max(ans, g[i] - g1[i]);
    }
    cout << ans;
}
